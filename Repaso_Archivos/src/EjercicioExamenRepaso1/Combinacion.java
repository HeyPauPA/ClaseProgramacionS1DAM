package EjercicioExamenRepaso1;

import java.util.ArrayList;

public class Combinacion 
{
	//------- Atributos -------
	private ArrayList<Integer> numeroCombinacion;

	//------ Constructores ----
	public Combinacion(ArrayList<Integer> numeroCombinacion) {
		super();
		this.numeroCombinacion = numeroCombinacion;
	}


	//-------- Getters/Setters --------
	public ArrayList<Integer> getNumeroCombinacion() {
		return numeroCombinacion;
	}
	
	public void setNumeroCombinacion(ArrayList<Integer> numeroCombinacion) {
		this.numeroCombinacion = numeroCombinacion;
	}


	
}
